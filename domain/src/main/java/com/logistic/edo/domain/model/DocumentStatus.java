package com.logistic.edo.domain.model;

/**
 * Жизненный цикл электронного документа в системе ЭДО.
 */
public enum DocumentStatus {

    DRAFT,       // Черновик, доступен для редактирования
    CREATED,     // Создан, ожидает отправки
    SENT,        // Отправлен контрагенту (оператору ЭДО)
    DELIVERED,   // Доставлен до контрагента
    SIGNED,      // Подписан всеми сторонами
    REJECTED,    // Отклонен контрагентом
    EXPIRED      // Срок действия истек
}
